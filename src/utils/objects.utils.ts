export const categoriasComUrl = Object.keys(
  livrosProd.reduce<{ [key: string]: string }>((acc, livro) => {
    if (!acc[livro.category]) {
      acc[livro.category] = livro.img;
    }
    return acc;
  }, {})
).map((category) => ({
  nome_categoria: category,
  image_url: livrosProd.find((livro) => livro.category === category)?.img,
}));

